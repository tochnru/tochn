      <h2 class="article-blog__h2">Виды геометрических условий, при уравнивании по способу условных измерений</h2>
      <p class="article-blog__txt"><span class="color-violet">1. Условие фигуры.</span> Условия этого вида возникают во
         всех случаях, когда в треугольнике измерены все три угла. Суммы внутренних углов треугольников должны быть
         равны 180°.</p>
      <p class="article-blog__txt"><span class="color-violet">2. Условие горизонта.</span> Это условие возникает в
         фигурах, в которых вокруг одного пункта измерены все углы. Такой фигурой является только центральная система
         (рис.1а). Сумма смежных углов вокруг этого пункта должна быть равна 360°.</p>
      <p class="article-blog__txt">$$∠1 +∠2+∠3+∠4+∠5=360°$$</p>
      <figure class="article-figure">
         <img class="article-figure__img"
            src="/img/article/ehto-kak/geometricheskie-usloviya-v-geodezicheskikh-setyakh/sheme-guvgs-1.webp"
            alt="Схема условий фигур">
         <figcaption class="article-figure__figcaption">Схема условий фигур</figcaption>
      </figure>
      <p class="article-blog__txt"><span class="color-violet">3. Условия сумм и дирекционных углов.</span> Условия сумм
         возникают в таких фигурах, где исходные стороны имеют твердую связь (рис. 1б). В данном случае сумма углов,
         измеренных на твердом пункте, должна быть равна разности твердых значений дирекционных углов. А именно:</p>
      <p class="article-blog__txt">$$∠1 +∠2+∠3=α2-α1$$</p>
      <p class="article-blog__txt">В условие суммы может входить любое количество измеренных углов больше одного.
         Условия дирекционных углов возникают в рядах треугольников, заключенных между двумя твердыми пунктами (рис. 2).
      </p>
      <p class="article-blog__txt">В данном случае должно быть удовлетворено равенство:</p>
      <p class="article-blog__txt">$$∠2-∠5+∠8-∠11+∠14-∠17=$$</p>
      <p class="article-blog__txt">$$=α2-α1$$</p>
      <figure class="article-figure">
         <img class="article-figure__img"
            src="/img/article/ehto-kak/geometricheskie-usloviya-v-geodezicheskikh-setyakh/sheme-guvgs-2.webp"
            alt="Схема условий фигур">
         <figcaption class="article-figure__figcaption">Схема условий фигур</figcaption>
      </figure>
      <p class="article-blog__txt"><span class="color-violet">4. Условие полюса.</span> Данное условие возникают в
         замкнутых фигурах, в которых все треугольники имеют общую вершину, и каждый из этих треугольников имеет по две
         общие стороны. Такими фигурами являются центральная система и геодезический четырехугольник. Полюсом в
         центральной системе служит пункт, расположенный внутри системы (общая вершина всех четырехугольников), полюсом
         в геодезическом четырехугольнике может служить точка пересечения диагоналей или одна из вершин данной фигуры
         (рис. 3а).</p>
      <figure class="article-figure">
         <img class="article-figure__img"
            src="/img/article/ehto-kak/geometricheskie-usloviya-v-geodezicheskikh-setyakh/sheme-guvgs-3.webp"
            alt="Схема условий фигур">
         <figcaption class="article-figure__figcaption">Схема условий фигур</figcaption>
      </figure>
      <p class="article-blog__txt">Применив теорему синусов (рис.3а), можно записать следующие равенства (в скобках даны
         номера углов по схеме):</p>
      <p class="article-blog__txt">$$a = b*{sin(6) \over sin(4)},$$</p>
      <p class="article-blog__txt">$$a = c*{sin(9)*sin(6) \over sin(7)*sin(4)},$$</p>
      <p class="article-blog__txt">$$a = d*{sin(12)*sin(9)*sin(6) \over sin(10)*sin(7)*sin(4)},$$</p>
      <p class="article-blog__txt">Разделив обе части последнего равенства на значение стороны а, получим:</p>
      <p class="article-blog__txt">$${sin(2)*sin(12)*sin(9)*sin(6) \over sin(1)*sin(10)*sin(7)*sin(4)}=1$$</p>
      <p class="article-blog__txt">Из приведенного равенства можно вывести следующее правило: в условном уравнении
         полюса учувствуют произведения синусов углов при наружных вершинах треугольников, причем в числителе – все
         левые (или правые) углы, в знаменателе – все правые (или левые) углы, если, стоя на пунктах, смотреть на точку
         полюса.</p>
      <p class="article-blog__txt"><span class="color-violet">5. Базисные условия.</span>Данный вид условий возникает в
         тех фигурах, в которых имеется две или более твердые стороны (рис. 4). В таких фигурах каждая пара твердых
         сторон одно базисное условие. В составлении базисных условий придерживаются того же принципа, что и при
         составлении полюсных условий: отойдя от одной стороны, необходимо получить соседнюю твердую сторону.</p>
      <figure class="article-figure">
         <img class="article-figure__img"
            src="/img/article/ehto-kak/geometricheskie-usloviya-v-geodezicheskikh-setyakh/sheme-guvgs-4.webp"
            alt="Схема условий фигур">
         <figcaption class="article-figure__figcaption">Схема условий фигур</figcaption>
      </figure>
      <p class="article-blog__txt">Применив теорему синусов, получим:</p>
      <p class="article-blog__txt">$$a = {b*sin(1)*sin(6) \over sin(3)*sin(4)},$$</p>
      <p class="article-blog__txt">или разделив обе части на значение стороны а, получим:</p>
      <p class="article-blog__txt">$${b*sin(1)*sin(6) \over a*sin(3)*sin(4)}=1$$</p>
      <p class="article-blog__txt">Аналогично составляют все базисные условия в любой подобной системе, при этом
         количество базисных условий всегда бывает на одно меньше количества твердых сторон.</p>
